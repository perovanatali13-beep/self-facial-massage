export function classify(sums: Record<SectionKey, number>): string {
  const oily = sums.oily >= 27 ? "O" : "D";
  const sensitive = sums.sensitive >= 30 ? "S" : "R";
  const pigment = sums.pigment >= 29 ? "P" : "N";
  const wrinkle = sums.wrinkle >= 41 ? "W" : "T";
  return `${oily}${sensitive}${pigment}${wrinkle}`;
}

